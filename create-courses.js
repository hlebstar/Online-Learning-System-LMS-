let inserted = 0;

courses.forEach((course) => {
  db.run(
    `INSERT INTO courses (title, description, weight) VALUES (?, ?, ?)`,
    [course.title, course.description, course.weight],
    function(err) {
      if (err) {
        if (err.message.includes('UNIQUE')) {
          console.log(`Курс "${course.title}" уже существует`);
        } else {
          console.log(`Ошибка: ${err.message}`);
        }
      } else {
        inserted++;
        console.log(` Добавлен курс: ${course.title}`);
        // Добавляем уроки для каждого курса
        addLessonsForCourse(this.lastID, course.title);
      }
    }
  );
});

// Добавляем уроки для курса
function addLessonsForCourse(courseId, courseTitle) {
  const lessons = getLessonsForCourse(courseTitle);
  
  lessons.forEach((lesson, index) => {
    db.run(
      `INSERT INTO lessons (course_id, title, content, duration, order_number) VALUES (?, ?, ?, ?, ?)`,
      [courseId, lesson.title, lesson.content, lesson.duration || 10, index + 1],
      function(err) {
        if (err) {
          console.log(`   Ошибка добавления урока "${lesson.title}": ${err.message}`);
        } else {
          console.log(`  Добавлен урок: ${lesson.title}`);
        }
      }
    );
  });
}

